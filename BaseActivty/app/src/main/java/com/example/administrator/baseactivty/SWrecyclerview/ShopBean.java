public class ShopBean {
    private int img;
    private String title;
    private int precent;
    private int total;
    private int shenyu;

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getPrecent() {
        return precent;
    }

    public void setPrecent(int precent) {
        this.precent = precent;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getShenyu() {
        return shenyu;
    }

    public void setShenyu(int shenyu) {
        this.shenyu = shenyu;
    }
}
